package com.example.demo.eventdb;

public class EventStudentEvaluation {

     private String studentId;
    private  String studentName;
    private  String studentNumber;
    private  String  timeEvaluated;

    public String getStudentId() {
        return studentId;
    }

    public void setStudentId(String studentId) {
        this.studentId = studentId;
    }

    public String getStudentName() {
        return studentName;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

    public String getStudentNumber() {
        return studentNumber;
    }

    public void setStudentNumber(String studentNumber) {
        this.studentNumber = studentNumber;
    }

    public String getTimeEvaluated() {
        return timeEvaluated;
    }

    public void setTimeEvaluated(String timeEvaluated) {
        this.timeEvaluated = timeEvaluated;
    }
}
