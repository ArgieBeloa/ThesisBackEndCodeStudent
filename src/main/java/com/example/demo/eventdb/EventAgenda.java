package com.example.demo.eventdb;

public class EventAgenda {
    private String agendaTime;
    private String agendaTitle;
    private String agendaHost;

    public String getAgendaHost() {
        return agendaHost;
    }

    public void setAgendaHost(String agendaHost) {
        this.agendaHost = agendaHost;
    }

    public String getAgendaTime() {
        return agendaTime;
    }

    public void setAgendaTime(String agendaTime) {
        this.agendaTime = agendaTime;
    }

    public String getAgendaTitle() {
        return agendaTitle;
    }

    public void setAgendaTitle(String agendaTitle) {
        this.agendaTitle = agendaTitle;
    }
}
